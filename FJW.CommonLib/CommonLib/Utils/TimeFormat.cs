namespace FJW.CommonLib.Utils
{
    /// <summary>
    /// 时间转字符串
    /// </summary>
    public class TimeFormat
    {
        /// <summary>
        /// 年-月-日 时:分:秒
        /// </summary>
        public const string YMDHMS = "yyyy-MM-dd HH:mm:ss";

        /// <summary>
        /// 年-月-日 时:分
        /// </summary>
        public const string YMDHM = "yyyy-MM-dd HH:mm";

        /// <summary>
        /// 年-月-日 时
        /// </summary>
        public const string YMDH = "yyyy-MM-dd HH";

        /// <summary>
        /// 年-月-日
        /// </summary>
        public const string YMD = "yyyy-MM-dd";

        /// <summary>
        /// 时:分
        /// </summary>
        public const string HM = "HH:mm";

        /// <summary>
        /// 时:分:秒
        /// </summary>
        public const string HMS = "HH:mm:ss";

        /// <summary>
        /// 分:秒
        /// </summary>
        public const string MS = "mm:ss";

        /// <summary>
        /// 年月日时分秒(缩减)
        /// </summary>
        public const string YMDHMS_Sub = "yyyyMMddHHmmss";

        /// <summary>
        /// 年月日时分(缩减)
        /// </summary>
        public const string YMDHM_Sub = "yyyyMMddHHmm";

        /// <summary>
        /// 年月日时(缩减)
        /// </summary>
        public const string YMDH_Sub = "yyyyMMddHH";

        /// <summary>
        /// 年月日(缩减)
        /// </summary>
        public const string YMD_Sub = "yyyyMMdd";

        /// <summary>
        /// 时分(缩减)
        /// </summary>
        public const string HM_Sub = "HHmm";

        /// <summary>
        /// 时分秒(缩减)
        /// </summary>
        public const string HMS_Sub = "HHmmss";

        /// <summary>
        /// 分秒(缩减)
        /// </summary>
        public const string MS_Sub = "mmss";
    }
}
